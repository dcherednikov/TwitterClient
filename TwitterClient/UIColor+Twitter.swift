import UIKit

extension UIColor {
    class var twitterBlue: UIColor {
        return UIColor(colorLiteralRed: 61/255,
                       green: 167/255,
                       blue: 244/255,
                       alpha: 1)
    }
    
    class var twitterGray: UIColor {
        return UIColor(colorLiteralRed: 230/255,
                       green: 230/255,
                       blue: 230/255,
                       alpha: 1)
    }
    
    class var twitterLightGray: UIColor {
        return UIColor(colorLiteralRed: 130/255,
                       green: 130/255,
                       blue: 130/255,
                       alpha: 1)
    }
    
    class var twitterBackgroundGray:  UIColor {
        return UIColor(colorLiteralRed: 232/255,
                       green: 236/255,
                       blue: 241/255,
                       alpha: 1)
    }
}

